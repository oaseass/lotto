export function dailySeed(iljuChar: string, date: Date, salt: number): number {
  const iljuIdx = '갑을병정무기경신임계'.indexOf(iljuChar) + 1
  const dateNum = date.getFullYear() * 10000 + (date.getMonth() + 1) * 100 + date.getDate()
  return Math.abs((dateNum * 31337 + iljuIdx * 7919 + salt) | 0)
}

export function getDailyFortune(iljuChar: string, date: Date): string {
  const ohaeng = CHEONGAN_OHAENG[iljuChar] ?? '토'
  const pool = DAILY_FORTUNE_POOL[ohaeng]
  return pool[dailySeed(iljuChar, date, 0) % pool.length]
}
